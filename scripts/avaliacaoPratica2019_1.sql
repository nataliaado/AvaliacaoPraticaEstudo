CREATE SCHEMA `avaliacaopratica` ;

--DDLs 
CREATE TABLE `avaliacaopratica`.`montadora` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `nome` VARCHAR(255) NOT NULL,
  `pais` VARCHAR(255) NOT NULL,
  `cnpj` VARCHAR(14) NOT NULL,
  PRIMARY KEY (`id`));
  
  CREATE TABLE `avaliacaopratica`.`carro` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `modelo` VARCHAR(255) NOT NULL,
  `ano` INT NOT NULL,
  `valor` DOUBLE NOT NULL,
  `placa` VARCHAR(8) NOT NULL,
  `idmontadora` INT NOT NULL,
  PRIMARY KEY (`id`),
  CONSTRAINT `idmontadora`
    FOREIGN KEY (`idmontadora`)
    REFERENCES `avaliacaopratica`.`montadora` (`id`)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION);

